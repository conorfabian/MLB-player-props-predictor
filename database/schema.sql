create or replace function public.publish_daily_board(
    p_slate_date date,
    p_model_version text,
    p_status text,
    p_metadata jsonb,
    p_picks jsonb
)
returns void
language plpgsql
as $$
declare
    v_board_id bigint;
begin
    insert into public.daily_boards (
        slate_date,
        generated_at,
        model_version,
        status,
        metadata
    )
    values (
        p_slate_date,
        now(),
        p_model_version,
        p_status,
        coalesce(p_metadata, '{}'::jsonb)
    )
    on conflict (slate_date)
    do update set
        generated_at = excluded.generated_at,
        model_version = excluded.model_version,
        status = excluded.status,
        metadata = excluded.metadata
    returning id into v_board_id;

    delete from public.board_picks
    where board_id = v_board_id;

    insert into public.board_picks (
        board_id,
        rank,
        player_name,
        team,
        opponent,
        prop_type,
        line,
        side,
        model_probability,
        game_time,
        result_status,
        prop_snapshot_id,
        model_run_id,
        provider,
        bookmaker_key,
        metadata
    )
    select
        v_board_id,
        pick.rank,
        pick.player_name,
        pick.team,
        pick.opponent,
        pick.prop_type,
        pick.line,
        pick.side,
        pick.model_probability,
        pick.game_time,
        pick.result_status,
        pick.prop_snapshot_id,
        pick.model_run_id,
        pick.provider,
        pick.bookmaker_key,
        coalesce(pick.metadata, '{}'::jsonb)
    from jsonb_to_recordset(p_picks) as pick (
        rank integer,
        player_name text,
        team text,
        opponent text,
        prop_type text,
        line double precision,
        side text,
        model_probability double precision,
        game_time timestamptz,
        result_status text,
        prop_snapshot_id bigint,
        model_run_id uuid,
        provider text,
        bookmaker_key text,
        metadata jsonb
    );
end;
$$;

