create table if not exists public.model_runs (
    id uuid primary key default gen_random_uuid(),
    ingestion_run_id uuid not null
        references public.prop_ingestion_runs(id),
    run_type text not null,
    model_version text not null,
    feature_version text not null,
    started_at timestamptz not null default now(),
    completed_at timestamptz,
    status text not null
        check (status in ('running', 'completed', 'failed')),
    candidate_count integer not null default 0,
    published_count integer not null default 0,
    error_message text,
    metadata jsonb not null default '{}'::jsonb
);

create table if not exists public.board_picks (
    id bigint generated by default as identity primary key,
    board_id bigint not null
        references public.daily_boards(id)
        on delete cascade,

    rank integer not null
        check (rank between 1 and 10),

    player_name text not null,
    team text not null,
    opponent text not null,

    prop_type text not null default 'hits',
    line double precision not null,
    side text not null default 'over'
        check (side in ('over', 'under')),

    model_probability double precision not null
        check (
            model_probability >= 0
            and model_probability <= 1
        ),

    game_time timestamptz,

    result_status text not null default 'pending'
        check (
            result_status in (
                'pending',
                'hit',
                'miss',
                'push',
                'postponed',
                'canceled'
            )
        ),

    prop_snapshot_id bigint
        references public.prop_snapshots(id),
    model_run_id uuid
        references public.model_runs(id),
    provider text,
    bookmaker_key text,
    metadata jsonb not null default '{}'::jsonb,
    actual_value double precision,
    graded_at timestamptz,
    grading_metadata jsonb not null default '{}'::jsonb,

    unique (board_id, rank)
);

create table if not exists public.candidate_predictions (
    id bigint generated by default as identity primary key,
    model_run_id uuid not null
        references public.model_runs(id)
        on delete cascade,
    prop_snapshot_id bigint not null
        references public.prop_snapshots(id),
    predicted_probability double precision not null
        check (
            predicted_probability >= 0
            and predicted_probability <= 1
        ),
    rank integer,
    eligible boolean not null,
    exclusion_reason text,
    created_at timestamptz not null default now(),
    unique (model_run_id, prop_snapshot_id)
);

